public class Atoz {
    public static void main(String []args){
        char i;
        for(i='a';i<'z';i++){
            System.out.print(i);
            System.out.print(" ");
        }
        for(i='A';i<'Z';i++){
            System.out.print(i);
            System.out.print(" ");
        }
    }
    
}
